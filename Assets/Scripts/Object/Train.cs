using UnityEngine;
using System.Collections;
//========================================================================
// ■ Train
//------------------------------------------------------------------------
//	電車のクラス。
//========================================================================

public class Train : MonoBehaviour {
	//--------------------------------------------------------------------
	// ● メンバ変数
	//--------------------------------------------------------------------
	public float move_speed = 19;
	//--------------------------------------------------------------------
	// ● 初期化
	//--------------------------------------------------------------------
	void Start() {
	}
	//--------------------------------------------------------------------
	// ● 更新
	//--------------------------------------------------------------------
	void Update() {
		transform.position += transform.forward * move_speed * Time.deltaTime;
	}
}