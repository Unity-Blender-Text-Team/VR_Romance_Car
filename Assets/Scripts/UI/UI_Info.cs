using UnityEngine;
using UnityEngine.UI;
using System.Collections;
//========================================================================
// ■ UI_Info
//------------------------------------------------------------------------
//	操作説明のUIクラス。
//========================================================================

public class UI_Info : MonoBehaviour {
	//--------------------------------------------------------------------
	// ● メンバ変数
	//--------------------------------------------------------------------
	CanvasGroup group;
	float end_second;
	//--------------------------------------------------------------------
	// ● 初期化
	//--------------------------------------------------------------------
	void Start() {
		group = GetComponent<CanvasGroup>();
		group.alpha = 1;
		end_second = 10 + Time.time;
	}
	//--------------------------------------------------------------------
	// ● 更新
	//--------------------------------------------------------------------
	void Update() {
		if (end_second < Time.time)
			group.alpha = 0;
	}
}