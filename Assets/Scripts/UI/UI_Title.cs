using UnityEngine;
using UnityEngine.UI;
using System.Collections;
using UnityEngine.SceneManagement;
//========================================================================
// ■ UI_Title
//------------------------------------------------------------------------
//	タイトルのUIクラス。
//========================================================================

public class UI_Title : MonoBehaviour {
	//--------------------------------------------------------------------
	// ● メンバ変数
	//--------------------------------------------------------------------

	//--------------------------------------------------------------------
	// ● 初期化
	//--------------------------------------------------------------------
	void Start() {
	}
	//--------------------------------------------------------------------
	// ● 更新
	//--------------------------------------------------------------------
	void Update() {
	}
	//--------------------------------------------------------------------
	// ● ボタンが押されたコールバック関数
	//--------------------------------------------------------------------
	public void on_click(Button button) {
		switch (button.name) {
			case "Button_Start":
				SceneManager.LoadScene("Test_Field");
				break;
			case "Button_End":
				Application.Quit();
				break;
		}
	}
}
